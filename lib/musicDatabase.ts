// Retourne N titres aléatoires, optionnellement filtrés par genre
export function getRandomTracks(n: number = 15, genre?: string): typeof musicDatabase {
  const pool = genre ? musicDatabase.filter(t => t.genre === genre) : musicDatabase;
  return [...pool].sort(() => Math.random() - 0.5).slice(0, n);
}

